package fx.models;

public class ModelStudentTwo {

    private String skole;
    private String kull;

    public ModelStudentTwo(String skole, String kull) {
        this.skole = skole;
        this.kull = kull;
    }

    public String getSkole() {
        return skole;
    }

    public void setSkole(String skole) {
        this.skole = skole;
    }

    public String getKull() {
        return kull;
    }

    public void setKull(String kull) {
        this.kull = kull;
    }
}
